/**
 * Get relevant bullet point suggestions based on job title
 * @param {string} jobTitle - The job title to match against
 * @returns {Array<string>} - Array of suggested bullet points
 */
export function getBulletSuggestions(jobTitle) {
  if (!jobTitle || typeof jobTitle !== 'string') {
    return bulletPointSuggestions.default
  }

  const title = jobTitle.toLowerCase()

  // Find matching category based on keywords in job title
  for (const [keyword, suggestions] of Object.entries(bulletPointSuggestions)) {
    if (title.includes(keyword)) {
      return suggestions
    }
  }

  // Return default suggestions if no match found
  return bulletPointSuggestions.default
}
